# Function to load quiz questions from a file
def load_questions(filename="questions.json"):
    try:
        with open(filename, "r") as file:
            questions = json.load(file)
        print(f"‚úÖ {len(questions)} questions loaded successfully!")
        return questions
    except FileNotFoundError:
        print("‚ùå Error: Question file not found.")
        return []

# Function to display a question and get user's input
def ask_question(question_data):
    print("\n" + question_data["question"])
    for i, option in enumerate(question_data["options"], start=1):
        print(f"{i}. {option}")

    while True:
        try:
            choice = int(input("Enter your answer (1-4): "))
            if 1 <= choice <= len(question_data["options"]):
                return question_data["options"][choice - 1]
            else:
                print("‚ö†Ô∏è Please enter a number between 1 and 4.")
        except ValueError:
            print("‚ö†Ô∏è Invalid input. Please enter a number.")

# Function to run the quiz
def run_quiz(questions):
    print("\nüéØ Welcome to the Python Quiz Game!")
    print("====================================")

    score = 0
    random.shuffle(questions)

    for q in questions:
        user_answer = ask_question(q)
        if user_answer.lower() == q["answer"].lower():
            print("‚úÖ Correct!\n")
            score += 1
        else:
            print(f"‚ùå Incorrect! The correct answer was: {q['answer']}\n")

    print("====================================")
    print(f"üèÜ Quiz Complete! Your Final Score: {score}/{len(questions)}")
    percentage = (score / len(questions)) * 100
    if percentage == 100:
        print("üåü Excellent! Perfect Score!")
    elif percentage >= 70:
        print("üëè Great job! You did really well!")
    elif percentage >= 50:
        print("üôÇ Good effort! Keep practicing.")
    else:
        print("üí° Needs Improvement. Try again!")

# Load questions and start the quiz
questions = load_questions("questions.json")
if questions:
    run_quiz(questions)

